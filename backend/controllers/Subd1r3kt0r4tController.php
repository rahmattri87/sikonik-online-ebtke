    public function actionGetsubdir($id) {
        $countPosts = Subd1r3kt0r4t::find()
             ->where(['kd_direktorat' => $id])
             ->count();
        $posts = Subd1r3kt0r4t::find()
             ->where(['kd_direktorat' => $id])
             ->groupBy('kd_subdir')
             ->orderBy('kd_subdir ASC')
             ->all();
