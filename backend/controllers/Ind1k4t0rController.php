    public function actionGetindikator($id) {
        $countPosts = Ind1k4t0r::find()
             ->where(['kd_subdir' => $id])
             ->count();
        $posts = Ind1k4t0r::find()
             ->where(['kd_subdir' => $id])
             ->groupBy('id_indikator')
             ->orderBy('id_indikator ASC')
             ->all();
        if($countPosts>0) {
             echo "<option value='0'>Pilih ID Sub Direktorat</option>";
             foreach($posts as $post){
                  echo "<option value='".$post->id_indikator."'>".$post->id_indikator." - ".$post->isi_indikator."</option>";
             }             
        }
        else{
             echo "<option>Pilih ID Sub Direktorat</option>";
        }
